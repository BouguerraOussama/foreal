<?php
require 'dbconfig.php';
class user
{
    public $username;
    public $firstName;
    public $lastName;
    public $email;
    public $password;
    public $phoneNumber;
    public $birthdate;
    public $gender;

    /**
     * Get the value of username
     */
    public function getUsername()
    {
        return $this->username;
    }

    /**
     * Set the value of username
     *
     * @return  self
     */
